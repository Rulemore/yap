#include <iostream>

using namespace std;

int main() {
  int n, a;
  int positiveSum = 0, negativeN = 0;
  cout << "Программа для подсчета сумм отрицательных и положительных элементов "
          "последовательности\n";
  cout << "-------------------------------------------------\n";
  cout << "Введите число n - длину последовательности целых чисел > ";
  cin >> n;
  for (int i = 0; i < n; i++) {
    cout << "Введите целое число a - член последовательности > ";
    cin >> a;
    if (a >= 0) {
      positiveSum += a;
    } else {
      negativeN++;
    }
  }
  cout << "-------------------------------------------------\n";
  cout << "Сумма положительных элементов последовательности = " << positiveSum
       << endl;
  cout << "Число отрицательных элементов последовательности = " << negativeN
       << endl;
  return 0;
}